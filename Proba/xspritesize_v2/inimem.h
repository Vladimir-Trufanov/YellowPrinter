/*
Какие факторы влияют на выбор ширины спрайта для CYD (ESP32 + TFT_eSPI)?
------------------------------------------------------------------------
- Объём доступной RAM. Спрайт в памяти хранится как массив пикселей: для 16‑битного цвета (RGB565) это ширина × высота × 2 байта. 
Если спрайт слишком широкий, он может не поместиться в свободную кучу (heap) или сильно её фрагментировать. Для ESP32 это критично: 
даже если формально хватает памяти, при активной отрисовке и аллокациях программа может падать с ESP_ERR_NO_MEM.

- Размер экрана и видимая область. Часто нет смысла делать спрайт шире экрана (например, 320 px): всё, что выходит за границы, не видно, 
а память и время на отрисовку тратятся. Исключение — скролл, параллакс, анимация «выезда» объекта: тогда спрайт делают с запасом.

- Формат цвета и битность. RGB565 (2 байта/пиксель) — самый частый вариант. Если перейти на 1‑битный монохром или палитру, можно сильно 
экономить память, но это меняет качество и усложняет подготовку графики.

- Производительность отрисовки. Копирование широких строк пикселей (DMA или простой memcpy) линейно зависит от ширины. 
Чем шире спрайт, тем дольше он рисуется. В играх это напрямую влияет на FPS.

- Выравнивание и оптимизация библиотеки TFT_eSPI. Библиотека лучше работает, когда ширина кратна 16 или 32 пикселям: 
так проще использовать блочные операции и избегать «хвостов» по краям строк. Это не строго обязательно, но заметно ускоряет вывод.

- Структура спрайт‑листа (sprite sheet). Если вы храните много кадров анимации в одном большом изображении, ширина листа должна 
быть кратна ширине кадра и учитывать количество кадров в строке. Это упрощает адресацию и обрезку отдельных кадров.

- Источник графики и подготовка. Растровые картинки из редактора могут иметь «лишние» поля, прозрачные зоны или некратные размеры.
Их лучше обрезать и подгонять под целевые размеры ещё на этапе подготовки, чтобы не тратить память и CPU в рантайме.

- Динамическое создание vs загрузка из PROGMEM/SPIFFS. Если спрайт создаётся в RAM «на лету», лимит ещё жёстче. 
Если он лежит в PROGMEM (как статический массив) и копируется в буфер по частям, можно работать с более широкими 
изображениями, но усложняется логика отрисовки.

- Конкуренция за ресурсы с другими задачами. ESP32 часто делает не только графику, но и Wi‑Fi, логирование, обработку датчиков. 
Большой спрайт может временно «съесть» почти всю свободную память и вызвать проблемы у других подсистем.

Практические рекомендации для Arduino/ESP32
-------------------------------------------
Для типичного экрана 320×240 держите ширину спрайта не больше 320, а лучше — в пределах 128–256 px для одиночных объектов.
Для спрайт‑листов подбирайте ширину листа так, чтобы в строку помещалось целое число кадров (например, 4 кадра по 64 px → ширина листа 256 px).
Всегда проверяйте потребление памяти: используйте heap_caps_get_free_size(MALLOC_CAP_8BIT) до и после создания спрайта.
Если видите тормоза — уменьшайте ширину и высоту, переходите на меньшую битность или рисуйте спрайт частями.
 
*/

